public class StarFigures{
    public static void combine(){
        System.out.println("*****");
        System.out.println("*****");
        System.out.println(" * * ");
        System.out.println("  *  ");
        System.out.println(" * * ");
    }
    public static void main(String[] args){
        combine();
        System.out.println();
        combine();
        System.out.println("*****");
        System.out.println("*****");
        System.out.println();
        System.out.println("  *");
        System.out.println("  *");
        System.out.println("  *");
        combine();
    }
}
